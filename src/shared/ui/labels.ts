export function labelFromMap<T extends string>(
  labels: Partial<Record<T, string>>,
  value: T
): string {
  return labels[value] ?? value;
}

export function armCodeLabel(code: string): string {
  if (code === "left") {
    return UI_LABELS.comparative.leftArm;
  }

  if (code === "right") {
    return UI_LABELS.comparative.rightArm;
  }

  return code;
}

export function armCodeWithInternalLabel(code: string): string {
  return `${armCodeLabel(code)} · código interno: ${code}`;
}
